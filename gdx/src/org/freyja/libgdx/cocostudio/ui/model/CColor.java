package org.freyja.libgdx.cocostudio.ui.model;

public class CColor {
	int R = 255;

	int G = 255;

	int B = 255;

	public int getR() {
		return R;
	}

	public void setR(int r) {
		R = r;
	}

	public int getG() {
		return G;
	}

	public void setG(int g) {
		G = g;
	}

	public int getB() {
		return B;
	}

	public void setB(int b) {
		B = b;
	}

}
